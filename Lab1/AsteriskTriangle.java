public class AsteriskTriangle { 
    public static void main(String[] args) { 
        // Print a triangular pattern with 1 asterisks 
        System.out.println("            *"); 
 
        // Print a triangular pattern with 3 asterisks 
        System.out.println("           ***"); 
 
        // Print a triangular pattern with 5 asterisks 
System.out.println("          *****"); 
// Print a triangular pattern with 7 asterisks 
System.out.println("         *******"); 
} 
} 